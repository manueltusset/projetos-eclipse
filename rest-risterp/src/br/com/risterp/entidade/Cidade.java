package br.com.risterp.entidade;

public class Cidade {
	private int ccidade;
	private String cidade;
	private String cep;
	private String uf;

	public int getCcidade() {
		return ccidade;
	}

	public void setCcidade(int ccidade) {
		this.ccidade = ccidade;
	}

	public String getCidade() {
		return cidade;
	}

	public void setCidade(String cidade) {
		this.cidade = cidade;
	}

	public String getCep() {
		return cep;
	}

	public void setCep(String cep) {
		this.cep = cep;
	}

	public String getUf() {
		return uf;
	}

	public void setUf(String uf) {
		this.uf = uf;
	}

}
